package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des paramètres param1 et param2 
	 * 
	 * @param param1
	 * @param param2
	 * @return int 
	 */
	public int addition(int param1, int param2){
		return param1+param2;
	}
	
	/** Méthode qui soustrait le parametre 2 du parametre 1
	 * 
	 * @param param1
	 * @param param2
	 * @return int
	 */
	public int soustraction(int param1, int param2){
		return param1-param2;
	}
}
